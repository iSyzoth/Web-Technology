// creating a function for a calculator
function sum(a,b){
    var result = a+b
    return result
}

function sub(a,b){
    var result = a-b
    return result
}

function mul(a,b){
    var result = a*b
    return result
}

function div(a,b){
    var result = a/b
    return result
}

function mod(a,b){
    var result = a%b
    return result
}

var res = sum(10,20) // calling the function 
console.log(res)

res = sub (10,20)
console.log(res)
